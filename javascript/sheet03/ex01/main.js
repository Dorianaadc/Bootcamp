/*Funciones de flecha (sheet03)

En JavaScript, las funciones de flecha (arrow functions) son una forma más
concisa de escribir funciones anónimas. Te dejo unos ejercicios para que
practiques con ellas.
EX01. Convertir una función anónima a una función de flecha
básica */

// Función normal
const suma = function(a, b) {
    return a + b;
    };

// Función de flecha
const suma2 = (a, b) => a + b;
